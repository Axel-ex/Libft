#include "libft.h"

int	ft_strequ(char *s1, char *s2)
{
	if (s1 != NULL || s2 != NULL)
	{
		while (*s1++ && *s2++ && *s1 == *s2)
		{
			if (*(s1 + 1) == '\0' && *(s2 + 1) == '\0')
				return (1);
		}
		return (0);
	}
	return (0);
}
