#include "libft.h"

int	ft_strnequ(char *s1, char *s2, size_t n)
{
	if (s1 != NULL || s2 != NULL)
	{
		while (*s1++ && *s2++ && *s1 == *s2 && n--)
		{
			if (*(s1 + 1) == '\0' && *(s2 + 1) == '\0')
				return (1);
		}
		return (0);
	}
	return (0);
}
