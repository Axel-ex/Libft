#include "libft.h"

int	ft_strnequ(char *s1, char *s2, size_t n)
{
	if (s1 && s2)
	{
		while (*s1 && *s2 && *s1 == *s2 && n)
		{
			s1++;
			s2++;
			n--;
		}
		if (!(*s1 - *s2))
			return (1);
	}
	return (0);
}
