#include "libft.h"

char	*ft_strdup(char *str)
{
	int		i;
	char	*cpy;

	cpy = (char *)malloc(sizeof(*cpy) * (ft_strlen(str) + 1));
	if (!cpy)
		return (0);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
